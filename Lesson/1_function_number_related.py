import math

pi = 3.14159
x = 1
y = 2
z = 3

print(round(pi))
print(math.ceil(pi)) # Round number up
print(math.floor(pi)) # Round number down
print(abs(pi)) # Get absolute value
print(pow(pi, 2)) # Raise the base number to power just like print(pi**2) can do.
print(math.sqrt(pi)) # Get the square root of the number
print(max(x, y, z, pi)) # Passes the maximum value
print(min(x, y, z, pi)) # Passes the minimum value


